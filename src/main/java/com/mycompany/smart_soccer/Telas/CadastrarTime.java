    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nomeTime = this.jTextField1.getText();
        String codGrupo = this.jTextField2.getText();
        Time t = new Time();
        
        if("".equals(nomeTime) || "".equals(codGrupo)) {
            JOptionPane.showMessageDialog(null, "Você precisa preencher os campos necessários primeiro");
        } else if (t.verificarTimeGrupo(codGrupo)){
            t.cadastrarTime(nomeTime, "Fase de Grupo", codGrupo);

            JOptionPane.showMessageDialog(null, """
                    Time cadastrado com Sucesso! 
                    Quando voc\u00ea tiver todos os 32 times j\u00e1 poder\u00e1 simular 
                    a Copa do Mundo!""", "Cadastro Concluído", JOptionPane.PLAIN_MESSAGE);

            this.jTextField1.setText("");
            this.jTextField2.setText("");
            this.setVisible(false);
            this.dispose();             
        } else {
            JOptionPane.showMessageDialog(null, "Máximo de Times por grupo atingido!\nCadastre esse time em outro grupo!",
                    "Ação não Permitida", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

